          <p>
Mobile is an exploding media and mobile advertising is following our collective eyeballs; already worth $3.5 Billion in the US and $18 Billion globally, its compound annual growth is projected to be an astounding <i><strong>49.7%</strong></i> through 2018 (the next fastest growing categories are Social and Desktop Video at 18% and 15% respectively.) <sup>1</sup>
</p>
<p>
And yet although mobile as a share of our overall media consumption already accounts for 25%, mobile advertising as a share of advertising lags significantly, at 9.8%.
          </p>
<p>
Maybe a "lag" as mobile advertising "catches up" with mobile media consumption?
</p>          

<p>
What about m-commerce, in which smartphones are only 1/3 as effective as a transaction platform as the desktop, with mobile platforms only responsible for 2.4% of US retail transactions in 2014 (e-marketer)?
</p>

<p>
According to Deloitte mobile is expected to be more important as an "influencer" of retail sales, through show-rooming, iBeacon and similar location based services, and 19% of all US retail transactions were influenced by mobile devices in 2014.
          </p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_1.jpg" aLt="" class="img-responsive">
</div>

<p>
And in any event as ApplePay and Google Wallter gain acceptance they will empower mobile devices as stand alone transaction vehicles.
</p>

<p>
However, Google's average mobile CPC (Cost Per Click) is decreasing at a rapid pace (10% Q4 2013 and 10% Q1 2014). Although Google no longer breaks down CPC by mobile, the fact that overall CPC is going down while mobile as a share of revenue goes up is a clear indication of the challenges Google faces in mobile advertising.
          </p>                              

<p>
What about app downloads, a huge driver for the growth so far in Mobile Advertising, with 350 million app downloads by Facebook alone between June 2012 and Q1 2014? 
</p>                              

<p>
There are problems in paradise there too. Average app CPI (Cost Per Install) rates are increasing ($1 to $2.39 for Android and iOS respectively in 2014) as the conversion efficiency of CPC led install campaigns decrease.
</p>                              

<p>
More importantly, app churn rates are huge and growing, with some analysts claiming an average 8% monthly churn in the 1st year, implying only 4% of users are retained within 12 months of download.</p>                              

<p>
This is not surprising, given the limited real estate on mobile devices, with no more than 30 installed apps on our devices on average. 
</p>

<h4>
So why is advertising so challenging on mobile?
</h4>

<p>
Screen size is sometimes a factor. Yet native formats such as the in-stream ads served by Facebook actually imply a larger average size of Facebook mobile ad units versus ad formats used in desktop display advertising.
</p>

<p>
An app centric mobile ecosystem versus the search centric universe of desktop browsing is part of the reason search based models do not work as well on mobile, at least so far.
</p>

<p>
But the single most important difference, the one that truly makes "Mobile Advertising" a category that requires a re-invention of the advertising model, is the average session length on a mobile device.
</p>

<p>
When we sit down to enjoy a movie, read a newspaper, listen to the radio or interact with our desktop, chances are we will be engaged for a significant period of time.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_2.jpg" aLt="" class="img-responsive">
</div>

<p>
Whereas although we interact with our smartphones often, the amount of time we spend interacting during a single session  (checking email, directions, weather, stocks, etc.) is usually very short.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_3.jpg" aLt="" class="img-responsive">
</div>

<p>
And our tolerance for being interrupted by an advert during short mobile sessions is very small.
</p>

<p>
This radically upends the traditional advertising model Quid Pro Quo: Free or Subsidized Content + Advertising = Conversion and Uplift.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_4.jpg" aLt="" class="img-responsive">
</div>

<h4>
Where do we go from here? 
</h4>

<p>
Most of us would not start a sales process by bragging to our potential customer about how great our product is, what its features are, how much better or cheaper than the competition it is, etc.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_5.jpg" aLt="" class="img-responsive">
</div>

<p>
Instead, when we do a really good job of selling, we start with our customer. We ask them questions, about their problems, their challenges, their routines. We engage them in a two way conversation about their issues. It's not about us. It's about them.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_6.jpg" aLt="" class="img-responsive">
</div>

<p>
And yet advertising has never worked that way. It has mostly been a one directional medium.
</p>

<p>
That's because, until recently, it simply was not possible to engage in an interactive and consultative sales process through advertising, because the technology and infrastructure did not exist to support it. 
</p>

<p>
But now it does.
</p>

<p>
Now it's possible to Gamify the Advertising Experience while enabling a two way interaction focused on the needs and wants of the prospect.
</p>

<p>
We call this approach "Mobile Quest Marketing". It allows for triggering an experiential engagement of targeted prospects at offline or online points of sale.
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_7.jpg" aLt="" class="img-responsive">
</div>

<p>
By leveraging the latest mobile and adtech technologies including location based services such as iBeacon, as well as the latest insights of behavioural science it is now possible to trigger a highly targeted experiential engagement that focuses on customer interests in a fun and rewarded set of activities.
</p>

<p>
This type of service allows us to sell CPA (Cost Per Action) performance based marketing. Most digital platforms to date are focused on selling leads (CPM, CPC, CPI), and have become highly effective at it. 
</p>

<p>
Now, as an industry we can focus on selling <i><strong>customers</strong></i> and <i><strong>performance</strong></i> (CPA).
</p>

<div class="text-center" style="margin-bottom: 20px;">
<img src="images/blog/2015-01-23/img_8.jpg" aLt="" class="img-responsive">
</div>

<p>
Using this new gamified advertising platform Eagle Ninja has already delivered 10X reductions in cost of customer acquisition, and a 20X improvement in engagement.
</p>
<p>
And we have only just begun.
</p>

